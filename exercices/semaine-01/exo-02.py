# Exercice 2 : Payer ou pas ?

# ✅ Exemple guidé :
age = int(input("Quel est votre âge ? "))

if age < 12:
    print("Entrée gratuite")
elif age <= 17:
	print("Tarif réduit")
elif age <= 64:
	print("Tarif normal")
else:
    print("Tarif senior")

